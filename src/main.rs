#![no_std]
#![no_main]

mod ssd1306;

extern crate rusty_rt;

use rusty_macros::main;
use rusty_peripheral::{
    cortex_m::Peripherals, exti::{ EXTI, line::Line }, gpio::{
        self,
        port::Port,
        pin::Pin,
        PinConfig,
        OutputType,
        Speed,
        Pull,
        InterruptType,
    }, i2c::{ self, I2CMode, SpeedMode }
};
use ssd1306::{ SSD1306Display, SSD1306_LCDHEIGHT, SSD1306_LCDWIDTH };

#[main]
fn main() -> ! {
    let mut nvic = Peripherals::nvic();
    let mut gpio_a = gpio::get_port(Port::A);
    let mut gpio_g = gpio::get_port(Port::G);
    let i2c1 = i2c::get_i2c1();

    gpio_a.enable_clock();
    gpio_g.enable_clock();

    gpio_a.init_pins(
        Pin::PIN0,
        PinConfig::Input(Speed::High, Pull::None, InterruptType::RisingEdge)
    );

    gpio_g.init_pins(
        Pin::PIN13,
        PinConfig::Output(OutputType::PushPull, Speed::High, Pull::None)
    );

    nvic.irq_enable(6);
    nvic.irq_set_priority(6, 13);

    i2c1.init(
        I2CMode::Master,
        SpeedMode::StandardMode,
        100_000,
        (Port::B, Pin::PIN6),
        (Port::B, Pin::PIN7)
    ).unwrap();

    let mut display = SSD1306Display::new(i2c1, 0x3cu8);
    display.init().unwrap();
    display.draw(&prep_image(RUST_LOGO)).unwrap();

    loop {
    }
}

#[no_mangle]
extern "C" fn __irq6_handler() {
    let exti = EXTI::get();

    let mask = exti.pr.get_pending_interrupts();
    if mask.is_set(Line::LINE0) {
        exti.pr.clear_pending_interrupts(mask | Line::LINE0);
    }

    let mut gpio_g = gpio::get_port(Port::G);
    gpio_g.toggle_pins(Pin::PIN13);
}

fn prep_image(
    buf: [u8; SSD1306_LCDWIDTH * SSD1306_LCDHEIGHT]
) -> [u8; (SSD1306_LCDWIDTH * SSD1306_LCDHEIGHT) / 8] {
    let mut res = [0; (SSD1306_LCDWIDTH * SSD1306_LCDHEIGHT) / 8];
    for i in 0..SSD1306_LCDHEIGHT / 8 {
        for j in 0..SSD1306_LCDWIDTH {
            let mut b = 0;
            for k in 0..8 {
                b |= buf[(i * 8 + k) * SSD1306_LCDWIDTH + j] << k;
            }

            res[i * SSD1306_LCDWIDTH + j] = b;
        }
    }

    res
}

